"""Benchmark <MODEL> on TabArena-v0.1: `setup` (launch on the cluster) + `eval` (leaderboard).

ONE file, TWO subcommands. `setup` and `eval` share the same ``BENCHMARK_NAME`` and
``PathSetup`` (``WORKSPACE`` + ``PYTHON_PATH``) defined once at the top, so the launch
and the evaluation can never drift apart::

    <PYTHON_PATH> tmp_scripts/run_<model>.py setup   # generate + emit the sbatch command(s)
    <PYTHON_PATH> tmp_scripts/run_<model>.py eval     # build the leaderboard from the results

Generated by the ``benchmark-model`` skill; lives under ``tmp_scripts/`` (gitignored) on purpose.

<MODEL> notes:
- Extra deps (install into the run venv before ``setup``): <PIP_EXTRA or "none">
- Problem types: <PROBLEM_TYPES>  (drives the eval ``subsets`` below)
- Foundation-model weights: <"prefetched from HF by the registry" if prefetch_weights else "none">
- <any per-run gotcha: partition choice, per-dataset time-limit bumps, fake VRAM, ...>
"""

from __future__ import annotations

import argparse
from pathlib import Path

from tabarena.benchmark.experiment import TabArenaV0pt1ExperimentBundle
from tabarena.benchmark.task.metadata import TaskSubset
from tabarena.contexts import TabArenaContext
from tabarena.evaluation import EvalMethod, TabArenaEvalConfig, run_eval
from tabflow_slurm import (
    GCPSlurmSetup,
    ModelJob,
    PathSetup,
    TabArenaBenchmarkPlan,
    TabArenaV0pt1ResourcesSetup,
)

# ── Shared identity — the ONE place these live; setup + eval both read them ──
BENCHMARK_NAME = "<model>_<DDMMYYYY>"  # cache key; reused unchanged across reruns
WORKSPACE = "/home/lennart_priorlabs_ai/workspace/benchmarking/tabarena_workspace"
PYTHON_PATH = "/home/lennart_priorlabs_ai/.venvs/tabarena_18062026/bin/python"

MODEL = "<Model>"  # registry name, e.g. "TabM" / "Nori" / "DenseLight"
NUM_CONFIGS = 0  # 0 = default config only (foundation models); an int or "all" for HPO


def _path_setup() -> PathSetup:
    return PathSetup(workspace=WORKSPACE, python_path=PYTHON_PATH)


def setup() -> None:
    """Generate the job JSON and emit the ``sbatch`` command(s) for the run."""
    plan = TabArenaBenchmarkPlan(
        benchmark_name=BENCHMARK_NAME,
        model_jobs=[
            ModelJob(
                models=(MODEL, NUM_CONFIGS),
                name="gpu",  # CPU model: drop the num_gpus override below and use name="cpu"
                resources={"num_gpus": 1},
                # GPU model whose static estimate reports VRAM on a VRAM-rich / RAM-poor
                # partition? add "fake_memory_for_estimates": <VRAM_GB>. Long tail? add
                # "time_limit": <seconds>. Omit both when the RAM estimate is already correct.
            ),
        ],
        context=TabArenaContext(),
        task_subset=TaskSubset(),  # no filter = the full task set (all splits); TaskSubset(subset="lite") for a quick first-split run
        experiment_bundle=TabArenaV0pt1ExperimentBundle(model_verbosity=2),
        path_setup=_path_setup(),
        resources_setup=TabArenaV0pt1ResourcesSetup(num_cpus=None, memory_limit=None),
        scheduler_setup=GCPSlurmSetup(gpu_partition="gpurtxpro6000spotinteractive"),
    )
    plan.setup_jobs()


def evaluate() -> None:
    """Build the TabArena-v0.1 leaderboard from the run's cached results."""
    config = TabArenaEvalConfig(
        benchmark_name=BENCHMARK_NAME,
        output_dir=_path_setup().get_output_path(BENCHMARK_NAME),
        methods=[EvalMethod(MODEL)],  # add result_suffix / only_load_cache if needed
        figure_output_dir=Path(__file__).parent / "eval_output" / BENCHMARK_NAME,
        # One entry per subset to score. `[]` = the full task set (overall leaderboard); add
        # per-type views as needed, e.g. [[], ["binary"], ["multiclass"], ["regression"]].
        subsets=[[]],
        figure_file_type=("pdf", "png"),  # emit PNGs alongside the PDFs (default: "pdf" only)
    )
    run_eval(config)


MODES = {"setup": setup, "eval": evaluate}
DEFAULT_MODE = "setup"  # bare invocation (no mode arg) runs this

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Run the setup or eval half of this benchmark.")
    parser.add_argument("mode", nargs="?", default=DEFAULT_MODE, choices=list(MODES))
    MODES[parser.parse_args().mode]()
